// default operator: prints the default value if lhv is falsy
const defaultVal = false || true
let count = 0
const zero = count || 'no count'
console.log(defaultVal, zero)

// nullish coalescing operator: prints the default value if lhv is null or undefined
const val = count ?? 'no count'
console.log(val)

// optional chaining: only accesses the property if the obj is defined
function getTotalReviews(book) {
  const goodReads = book.reviews.goodreads?.reviewsCount ?? 0;
  const librarything = book.reviews.librarything?.reviewsCount ?? 0;
  return goodReads + librarything
}
